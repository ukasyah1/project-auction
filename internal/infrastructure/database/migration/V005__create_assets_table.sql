CREATE TABLE CMS.ASSETS (
    ID VARCHAR(36) PRIMARY KEY,
    KODE_ASET VARCHAR(50) NOT NULL,
    NAMA_ASET VARCHAR(255) NOT NULL,
    NAMA_DEBITUR VARCHAR(255),
    NO_REK_PINJAMAN VARCHAR(50),
    TIPE_ASET_ID VARCHAR(36),
    ALAMAT VARCHAR(500),
    PROVINSI_ID VARCHAR(36),
    KOTA_ID VARCHAR(36),
    KECAMATAN VARCHAR(100),
    LUAS_TANAH NUMERIC(10),
    LUAS_BANGUNAN NUMERIC(10),
    NILAI_PASAR NUMERIC(19,2),
    NILAI_LIKUIDASI NUMERIC(19,2),
    LIMIT_LELANG NUMERIC(19,2),
    KPKNL_ID VARCHAR(36),
    JENIS_AGUNAN VARCHAR(100),
    NO_SERTIFIKAT VARCHAR(100),
    JENIS_SERTIFIKAT VARCHAR(50),
    STATUS_PENGHUNI VARCHAR(50),
    LINK_LELANG VARCHAR(500),
    LINK_MAPS VARCHAR(500),
    LATITUDE VARCHAR(50),
    LONGITUDE VARCHAR(50),
    STATUS VARCHAR(20) NOT NULL,
    START_DATE TIMESTAMP,
    END_DATE TIMESTAMP,
    ZONA_WAKTU VARCHAR(4),
    IMAGE_URLS TEXT,
    DESKRIPSI TEXT,
    FASILITAS TEXT,
    VIEW_COUNT NUMERIC(10) DEFAULT 0 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT UK_ASSETS_KODE_ASET UNIQUE (KODE_ASET),
    CONSTRAINT FK_ASSETS_TIPE FOREIGN KEY (TIPE_ASET_ID) REFERENCES CMS.M_TIPE_ASET (ID),
    CONSTRAINT FK_ASSETS_PROVINSI FOREIGN KEY (PROVINSI_ID) REFERENCES CMS.M_PROVINSI (ID),
    CONSTRAINT FK_ASSETS_KOTA FOREIGN KEY (KOTA_ID) REFERENCES CMS.M_KOTA (ID),
    CONSTRAINT FK_ASSETS_KPKNL FOREIGN KEY (KPKNL_ID) REFERENCES CMS.M_KPKNL (ID),
    CONSTRAINT CK_ASSETS_STATUS CHECK (STATUS IN ('PUBLISHED', 'DRAFT', 'SOLD', 'TAKEDOWN', 'LELANG')),
    CONSTRAINT CK_ASSETS_ZONA CHECK (ZONA_WAKTU IS NULL OR ZONA_WAKTU IN ('WIB', 'WITA', 'WIT'))
);

CREATE TABLE CMS.M_KECAMATAN (
    ID VARCHAR(36) PRIMARY KEY,
    KOTA_ID VARCHAR(36),
    NAMA_KECAMATAN VARCHAR(100),
    KODE_PREFIX VARCHAR(5),
    CONSTRAINT FK_M_KOTA FOREIGN KEY (KOTA_ID) REFERENCES CMS.M_KOTA (ID)
);


