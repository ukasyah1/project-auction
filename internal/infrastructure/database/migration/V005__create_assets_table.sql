CREATE TABLE CMS.ASSETS (
    ID VARCHAR2(36) PRIMARY KEY,
    KODE_ASET VARCHAR2(50) NOT NULL,
    NAMA_ASET VARCHAR2(255) NOT NULL,
    NAMA_DEBITUR VARCHAR2(255),
    NO_REK_PINJAMAN VARCHAR2(50),
    TIPE_ASET_ID VARCHAR2(36),
    ALAMAT VARCHAR2(500),
    PROVINSI_ID VARCHAR2(36),
    KOTA_ID VARCHAR2(36),
    KECAMATAN VARCHAR2(100),
    LUAS_TANAH NUMBER(10),
    LUAS_BANGUNAN NUMBER(10),
    NILAI_PASAR NUMBER(19,2),
    NILAI_LIKUIDASI NUMBER(19,2),
    LIMIT_LELANG NUMBER(19,2),
    KPKNL_ID VARCHAR2(36),
    JENIS_AGUNAN VARCHAR2(100),
    NO_SERTIFIKAT VARCHAR2(100),
    JENIS_SERTIFIKAT VARCHAR2(50),
    STATUS_PENGHUNI VARCHAR2(50),
    LINK_LELANG VARCHAR2(500),
    LINK_MAPS VARCHAR2(500),
    LATITUDE VARCHAR2(50),
    LONGITUDE VARCHAR2(50),
    STATUS VARCHAR2(20) NOT NULL,
    START_DATE TIMESTAMP,
    END_DATE TIMESTAMP,
    ZONA_WAKTU VARCHAR2(4),
    IMAGE_URLS CLOB,
    DESKRIPSI CLOB,
    FASILITAS CLOB,
    VIEW_COUNT NUMBER(10) DEFAULT 0 NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSDATE NOT NULL,
    UPDATED_AT TIMESTAMP DEFAULT SYSDATE NOT NULL,
    CONSTRAINT UK_ASSETS_KODE_ASET UNIQUE (KODE_ASET),
    CONSTRAINT FK_ASSETS_TIPE FOREIGN KEY (TIPE_ASET_ID) REFERENCES CMS.M_TIPE_ASET (ID),
    CONSTRAINT FK_ASSETS_PROVINSI FOREIGN KEY (PROVINSI_ID) REFERENCES CMS.M_PROVINSI (ID),
    CONSTRAINT FK_ASSETS_KOTA FOREIGN KEY (KOTA_ID) REFERENCES CMS.M_KOTA (ID),
    CONSTRAINT FK_ASSETS_KPKNL FOREIGN KEY (KPKNL_ID) REFERENCES CMS.M_KPKNL (ID),
    CONSTRAINT CK_ASSETS_STATUS CHECK (STATUS IN ('PUBLISHED', 'DRAFT', 'SOLD', 'TAKEDOWN', 'LELANG')),
    CONSTRAINT CK_ASSETS_ZONA CHECK (ZONA_WAKTU IS NULL OR ZONA_WAKTU IN ('WIB', 'WITA', 'WIT'))
);

CREATE TABLE CMS.M_KECAMATAN (
    ID VARCHAR2(36) PRIMARY KEY,
    KOTA_ID VARCHAR2(36),
    NAMA_KECAMATAN VARCHAR2(100),
    KODE_PREFIX VARCHAR2(5),
    CONSTRAINT FK_M_KOTA FOREIGN KEY (KOTA_ID) REFERENCES CMS.M_KOTA (ID)
);


