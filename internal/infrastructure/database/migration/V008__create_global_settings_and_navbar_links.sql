CREATE TABLE CMS.GLOBAL_SETTINGS (
    ID NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SETTING_KEY VARCHAR2(100) NOT NULL,
    VALUE_ID CLOB,
    VALUE_EN CLOB,
    UPDATED_AT TIMESTAMP DEFAULT SYSDATE NOT NULL,
    CONSTRAINT UK_GLOBAL_SETTINGS_KEY UNIQUE (SETTING_KEY)
);

CREATE TABLE CMS.NAVBAR_LINKS (
    ID VARCHAR2(36) PRIMARY KEY,
    PARENT_ID VARCHAR2(36),
    TITLE_ID VARCHAR2(100) NOT NULL,
    TITLE_EN VARCHAR2(100) NOT NULL,
    TARGET_URL VARCHAR2(255) NOT NULL,
    ORDER_INDEX NUMBER(5) NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CONSTRAINT FK_NAVBAR_LINKS_PARENT FOREIGN KEY (PARENT_ID) REFERENCES CMS.NAVBAR_LINKS (ID),
    CONSTRAINT CK_NAVBAR_LINKS_ACTIVE CHECK (IS_ACTIVE IN (0, 1))
);
