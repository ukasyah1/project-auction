CREATE TABLE CMS.GLOBAL_SETTINGS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SETTING_KEY VARCHAR(100) NOT NULL,
    VALUE_ID TEXT,
    VALUE_EN TEXT,
    UPDATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    CONSTRAINT UK_GLOBAL_SETTINGS_KEY UNIQUE (SETTING_KEY)
);

CREATE TABLE CMS.NAVBAR_LINKS (
    ID VARCHAR(36) PRIMARY KEY,
    PARENT_ID VARCHAR(36),
    TITLE_ID VARCHAR(100) NOT NULL,
    TITLE_EN VARCHAR(100) NOT NULL,
    TARGET_URL VARCHAR(255) NOT NULL,
    ORDER_INDEX NUMERIC(5) NOT NULL,
    IS_ACTIVE NUMERIC(1) DEFAULT 1 NOT NULL,
    CONSTRAINT FK_NAVBAR_LINKS_PARENT FOREIGN KEY (PARENT_ID) REFERENCES CMS.NAVBAR_LINKS (ID),
    CONSTRAINT CK_NAVBAR_LINKS_ACTIVE CHECK (IS_ACTIVE IN (0, 1))
);
